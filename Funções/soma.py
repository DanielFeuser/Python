# Definindo uma função de soma.
def soma (x,y,z): # Aqui definimos(def) uma função(soma) que teria 3 valores (x,y,z).
    return (x + y + z) # Aqui é o retorno(return) do que a função ira fazer.
print(soma(10,20,30))
